NINTH_CODE_PATTERN = re.compile(r"^\d+(?:_\d+)*")
ESTO_CODE_PATTERN = re.compile(r"^\d+(?:\.\d+)*")
SKIP_LABEL_TOKENS = ("_unallocated", "_x")
