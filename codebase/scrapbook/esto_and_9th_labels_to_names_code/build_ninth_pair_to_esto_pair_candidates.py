r"""

This mapping connects 9th sector/fuel pairs to ESTO flow/product pairs.
It is built from the most detailed non‑x 9th sector and fuel labels and includes some one‑to‑many and many‑to‑one relationships (but no many‑to‑many).
Main generator: codebase/scrapbook/esto_and_9th_labels_to_names_code/build_ninth_pair_to_esto_pair_candidates.py
9th flattening / projected‑nonzero pairs: codebase/scrapbook/esto_and_9th_labels_to_names_code/build_ninth_flattened_pairs.py
Independent flow/product mappings input: config/independent product flow mappings.xlsx

ninth_pair_exists tells you whether a real 9th sector/fuel pair exists for that row.
If ninth_pair_exists is False or blank, there is no 9th sector/fuel pair mapped to the existing ESTO flow/product pair.
These are likely very minor datapoints across APERC, but if you do need to map them then you can use what is in orig_9th_sector and orig_9th_fuel and copy/paste those values.

Do not overwrite C:\Users\Work\github\leap_utilities\config\ninth_pairs_to_esto_pairs_MASTER.csv since that contains a copy of the mappings, and some of them had to be done manually.
Most of the file was generated by code, but the master file includes manual fixes.

The mapping was generated in stages:

1. direct pair matching (build_ninth_pair_to_esto_pair_candidates.py)
2. independent flow/product mappings (config/independent product flow mappings.xlsx)
3. hierarchy backoff for strict projected‑nonzero output (build_ninth_pair_to_esto_pair_candidates.py, using 9th data hierarchy)

Because one‑to‑many mappings exist, converting from 9th to ESTO or ESTO to 9th requires splitting values.
Use the proportional allocation approach in codebase/ninth_projection_mapping.py (based on base‑year shares from the other dataset).

There is also a strict projected‑nonzero version that removes 9th pairs with zero values across projection years.
Use that if you only want pairs with non‑zero 9th projections, but note it can remove some ESTO‑only pairs.

"""



